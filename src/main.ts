import { NestFactory } from '@nestjs/core';
import { ValidationPipe } from '@nestjs/common';
import { DocumentBuilder, SwaggerModule } from '@nestjs/swagger';
import { AppModule } from './app.module';
import * as dotenv from 'dotenv';

// Load environment variables
dotenv.config();

async function bootstrap() {
  const app = await NestFactory.create(AppModule);

  // Enable global validation pipe
  app.useGlobalPipes(new ValidationPipe({
    transform: true,
    whitelist: true,
    forbidNonWhitelisted: true,
  }));

  // Swagger configuration
  const config = new DocumentBuilder()
    .setTitle('Cities API')
    .setDescription('A REST API for retrieving cities data by country')
    .setVersion('1.0')
    .addTag('cities')
    .addServer(`http://localhost:${process.env.PORT || 3000}`, 'Development server')
    .build();

  const document = SwaggerModule.createDocument(app, config);
  SwaggerModule.setup('api-docs', app, document, {
    swaggerOptions: {
      tagsSorter: 'alpha',
      operationsSorter: 'alpha',
    },
  });

  const port = process.env.PORT || 3000;
  await app.listen(port);
  console.log(`üöÄ Application is running on: http://localhost:${port}`);
  console.log(`üìö Swagger documentation available at: http://localhost:${port}/api-docs`);
  console.log(`üåç Environment: ${process.env.NODE_ENV || 'development'}`);
}
bootstrap();
